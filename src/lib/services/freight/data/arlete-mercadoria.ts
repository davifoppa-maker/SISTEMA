/** Código de mercadoria padrão quando o CEP não está em nenhuma faixa conhecida. */
export const MERCADORIA_PADRAO = 1;

/**
 * Resolve o código de mercadoria do SSW/Arlete a partir do CEP de destino.
 * Busca binária nas faixas da planilha; cai no padrão (001) se não achar.
 */
export function mercadoriaCodeForCep(cep: string | number): number {
  const n = Number(String(cep).replace(/\D/g, ""));
  if (!n) return MERCADORIA_PADRAO;
  let lo = 0, hi = RANGES.length - 1;
  while (lo <= hi) {
    const mid = (lo + hi) >> 1;
    const [ini, fim, cod] = RANGES[mid];
    if (n < ini) hi = mid - 1;
    else if (n > fim) lo = mid + 1;
    else return cod;
  }
  return MERCADORIA_PADRAO;
}
